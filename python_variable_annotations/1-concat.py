def concat(str1: str, str2: str) -> str:
    """
    This function takes a string str1 and a string str2 as arguments
    and returns a concatenated string.

    Args:
      str1: A string.
      str2: A string.

    Returns:
      The concatenation of str1 and str2.
    """

    return str1 + str2
